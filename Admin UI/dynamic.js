  function handleSuggestionNavigation(
    event,
    list,
    focusedIndex,
    setFocusedIndex,
    action
  ) {
    const { key } = event;
    const suggestions = list.querySelectorAll("li");

    if (key === "ArrowDown" || key === "ArrowUp") {
      event.preventDefault();
      focusedIndex =
        (focusedIndex + (key === "ArrowDown" ? 1 : -1) + suggestions.length) %
        suggestions.length;
      setFocusedIndex(focusedIndex);

      suggestions.forEach((suggestion, index) => {
        const button = suggestion.querySelector("button");
        button.classList.toggle("focused", index === focusedIndex);
        button.style.backgroundColor =
          index === focusedIndex ? "white" : "beige";
      });
      const focusedSuggestion = suggestions[focusedIndex];
      const focusedButton = focusedSuggestion
        ? focusedSuggestion.querySelector(".indi-btn")
        : 0;
      if (focusedButton) {
        focusedButton.style.backgroundColor = "white";
      }

      if (
        focusedSuggestion.offsetTop < list.scrollTop ||
        focusedSuggestion.offsetTop + focusedSuggestion.offsetHeight >
        list.scrollTop + list.offsetHeight
      ) {
        focusedSuggestion.scrollIntoView({
          behavior: "smooth",
          block: "nearest",
        });
      }
    } else if (
      key === "Enter" &&
      document.activeElement.id === event.target.id
    ) {
      const focusedButton = list.querySelector(".focused");
      if (focusedButton) {
        list.innerHTML = "";
        action(focusedButton.id, focusedButton.textContent);
      }
    }
  }

